package practice_10_12;

import java.util.Scanner;

public class For_02 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int c = sc.nextInt();
		
	    for(int i=0; i<=c; i++ ) {
	    	int a = sc.nextInt();
	    	int b = sc.nextInt();
	    	
	    	System.out.println(a + b);
	    }
	    sc.close();
	    
	    	    
	}

}
